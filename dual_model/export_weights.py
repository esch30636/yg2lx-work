def sanitize(name):
    return name.replace("/", "_").replace(".", "_").replace(":", "_").replace("-", "_")


# ═══════════════════════════════════════════════════════════════════════
# FP32 weight extractor for ONNX models
# ═══════════════════════════════════════════════════════════════════════

def extract_weights_fp32(model_path):
    """Return dict: c_var_name → (float32_array, comment_str) for all weights.

    Handles:
      - Gemm weights with transB=1: transpose from [out,in] to [in,out]
      - Conv weights: keep [out_ch, in_ch, kernel] layout (matches C conv1d)
      - Biases and 1D params: keep as-is
    """
    model = onnx.load(model_path)
    graph = model.graph

    # Build initializer dict
    init_map = {}
    for w in graph.initializer:
        init_map[w.name] = onnx.numpy_helper.to_array(w)

    # Build map: initializer name → whether to transpose (for Gemm with transB=1)
    transpose_set = set()
    for node in graph.node:
        if node.op_type == "Gemm":
            transB = False
            for attr in node.attribute:
                if attr.name == "transB" and attr.i == 1:
                    transB = True
            if transB and len(node.input) >= 2:
                w_name = node.input[1]   # weight is 2nd input of Gemm
                if w_name in init_map:
                    transpose_set.add(w_name)

    weights = {}

    for w in graph.initializer:
        name = w.name
        arr = onnx.numpy_helper.to_array(w)
        c_name = sanitize(name)

        # Skip reshape_shape tensors
        if "reshape_shape" in name:
            continue

        dtype = {1: "float32", 2: "uint8", 3: "int8", 6: "int32", 7: "int64"}.get(
            w.data_type, f"type_{w.data_type}")

        shape_str = " × ".join(str(d) for d in arr.shape)

        if name in transpose_set:
            # Gemm weight: ONNX stores [out,in], C expects [in,out]
            arr = arr.T.copy()
            comment = f"/* [{shape_str}] → transposed to [{(' × '.join(str(d) for d in arr.shape))}] {dtype} */"
        else:
            comment = f"/* [{shape_str}] {dtype} */"

        flat = arr.astype(np.float32).flatten()
        weights[c_name] = (flat, comment)

    return weights


def write_weight_header(weights, guard_name, out_path):
    """Write a C header file from extracted weights."""
    lines = []
    lines.append("/* Auto-generated by export_weights.py — DO NOT EDIT */")
    lines.append(f"/* Total arrays: {len(weights)} */")
    lines.append("")
    lines.append(f"#ifndef {guard_name}")
    lines.append(f"#define {guard_name}")
    lines.append("")
    lines.append('#include <stddef.h>')
    lines.append("")

    for c_name, (arr, comment) in sorted(weights.items()):
        lines.append(f"// {comment.strip()}")
        lines.append(f"static const float {c_name}[{len(arr)}] = {{")

        for i in range(0, len(arr), 8):
            chunk = arr[i:i+8]
            vals = ", ".join(
                f"{v:.8f}f" if abs(v) >= 1e-10 and abs(v) < 1e6
                else f"{v:.10e}f"
                for v in chunk
            )
            comma = "," if i + 8 < len(arr) else ""
            lines.append(f"    {vals}{comma}")

        lines.append("};")
        lines.append("")

    lines.append(f"#endif /* {guard_name} */")
    lines.append("")

    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    with open(out_path, "w") as f:
        f.write("\n".join(lines))

    total_floats = sum(len(arr) for arr, _ in weights.values())
    print(f"  Wrote: {out_path}  ({len(lines)} lines, {total_floats:,} float32 values, ~{total_floats*4/1024:.1f} KB)")


