# ═══════════════════════════════════════════════════════════════════════
# Scaler export
# ═══════════════════════════════════════════════════════════════════════

def write_scalers(out_dir):
    lines = []
    lines.append("/* Auto-generated by export_weights.py */")
    lines.append("#ifndef SCALERS_H")
    lines.append("#define SCALERS_H")
    lines.append("")

    # PINN feature scaler
    with open(PINN_SCALER, "rb") as f:
        sc = pickle.load(f)
    mean = sc.mean_.astype(np.float32)
    std = sc.scale_.astype(np.float32)
    std = np.where(std < 1e-8, 1.0, std)

    lines.append(f"/* PINN feature scaler (mean + std, each {len(mean)}-d) */")
    for label, arr in [("PINN_SCALER_MEAN", mean), ("PINN_SCALER_STD", std)]:
        flat = arr.flatten()
        lines.append(f"static const float {label}[{len(flat)}] = {{")
        for i in range(0, len(flat), 8):
            chunk = flat[i:i+8]
            vals = ", ".join(f"{v:.8f}f" for v in chunk)
            comma = "," if i + 8 < len(flat) else ""
            lines.append(f"    {vals}{comma}")
        lines.append("};")
        lines.append("")

    # CNN IC + gradient scalers
    with open(CNN_SCALER, "rb") as f:
        scs = pickle.load(f)

    for key in ["ic_scaler", "ig_scaler"]:
        sc = scs[key]
        mean = sc.mean_.astype(np.float32)
        std = sc.scale_.astype(np.float32)
        std = np.where(std < 1e-8, 1.0, std)
        c_prefix = "CNN_IC" if key == "ic_scaler" else "CNN_IG"

        for label, arr in [(f"{c_prefix}_SCALER_MEAN", mean), (f"{c_prefix}_SCALER_STD", std)]:
            flat = arr.flatten()
            lines.append(f"/* {key}: {label} */")
            lines.append(f"static const float {label}[{len(flat)}] = {{")
            for i in range(0, len(flat), 8):
                chunk = flat[i:i+8]
                vals = ", ".join(f"{v:.8f}f" for v in chunk)
                comma = "," if i + 8 < len(flat) else ""
                lines.append(f"    {vals}{comma}")
            lines.append("};")
            lines.append("")

    lines.append("#endif /* SCALERS_H */")

    path = os.path.join(out_dir, "scalers.h")
    with open(path, "w") as f:
        f.write("\n".join(lines))
    print(f"  Wrote: {path}  ({len(lines)} lines)")


