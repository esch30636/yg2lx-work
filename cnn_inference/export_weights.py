#!/usr/bin/env python3
"""
Export ONNX model weights to pure-C header with float32 dequantization.

Usage:
  python3 export_weights.py [output_dir]

Output:
  include/model_weights.h  — all weights dequantized to float32 C arrays
  include/model_meta.h     — architecture constants (shapes, layer params)
  include/gelu_consts.h    — GELU constants extracted from ONNX Constant nodes
"""

import onnx
import numpy as np
import sys, os

MODEL_PATH = "/home/legion/Downloads/battery_cnn_int8.onnx"
OUT_DIR = sys.argv[1] if len(sys.argv) > 1 else "/home/legion/yg2lx-work/cnn_inference/include"


def dtype_str(elem_type):
    return {1: "float", 2: "uint8", 3: "int8", 6: "int32", 7: "int64"}.get(elem_type, f"type_{elem_type}")


def sanitize(name):
    """Make a valid C identifier from an ONNX tensor name."""
    return name.replace("/", "_").replace(".", "_").replace(":", "_").replace("-", "_")


def write_weights(model, out_dir):
    """Extract weights, dequantize INT8→float32, write C header."""
    graph = model.graph

    # Build dict: initializer name → numpy array
    init = {}
    for w in graph.initializer:
        init[w.name] = onnx.numpy_helper.to_array(w)

    # Build dict: quantized_weight → (scale_name, zero_point_name)
    quant_map = {}  # weight_quantized_name → (scale_name, zp_name, float_name)
    for w in graph.initializer:
        name = w.name
        if name.endswith("_quantized"):
            base = name[:-len("_quantized")]
            scale_name = base + "_scale"
            zp_name = base + "_zero_point"
            float_name = base.replace("_quantized", "")  # e.g. onnx::Conv_190
            quant_map[name] = (scale_name, zp_name, float_name)

    lines = []
    lines.append("/* Auto-generated by export_weights.py — DO NOT EDIT */")
    lines.append(f"/* Model: battery_cnn_int8.onnx */")
    total_p = sum(onnx.numpy_helper.to_array(w).size for w in graph.initializer)
    lines.append(f"/* Total parameters: {total_p:,} */")
    lines.append("")
    lines.append("#ifndef MODEL_WEIGHTS_H")
    lines.append("#define MODEL_WEIGHTS_H")
    lines.append("")
    lines.append('#include <stddef.h>')
    lines.append("")

    # Process all initializers
    for w in sorted(graph.initializer, key=lambda x: x.name):
        name = w.name
        arr = onnx.numpy_helper.to_array(w)
        c_name = sanitize(name)

        # Skip reshape_shape tensors (not needed for inference)
        if "reshape_shape" in name:
            continue
        # Skip scale and zero_point — we dequantize at export time
        if name.endswith("_scale") or name.endswith("_zero_point"):
            continue

        # For quantized weights, dequantize
        if name.endswith("_quantized"):
            scale_name, zp_name, _ = quant_map[name]
            if scale_name in init and zp_name in init:
                scale = init[scale_name].item()
                zp = init[zp_name].item()
                arr_f32 = (arr.astype(np.float32) - zp) * scale
                arr = arr_f32.astype(np.float32)
                c_name = sanitize(name.replace("_quantized", ""))  # clean name
                comment = f"  /* [{(' × '.join(str(d) for d in arr_f32.shape))}] float32 (dequantized from int8, scale={scale:.8f}, zp={zp}) */"
            else:
                comment = f"  /* [{(' × '.join(str(d) for d in arr.shape))}] {dtype_str(w.data_type)} */"
        else:
            comment = f"  /* [{(' × '.join(str(d) for d in arr.shape))}] {dtype_str(w.data_type)} */"

        # Flatten to 1D C array
        flat = arr.flatten()
        lines.append(f"// {comment.strip()}")
        lines.append(f"static const float {c_name}[{len(flat)}] = {{")

        # Pretty-print: 8 values per line
        for i in range(0, len(flat), 8):
            chunk = flat[i:i+8]
            vals = ", ".join(f"{v:.8f}f" if abs(v) >= 1e-10 else f"{v:.10e}f" for v in chunk)
            comma = "," if i + 8 < len(flat) else ""
            lines.append(f"    {vals}{comma}")

        lines.append("};")
        lines.append("")

    lines.append("#endif /* MODEL_WEIGHTS_H */")
    lines.append("")

    os.makedirs(out_dir, exist_ok=True)
    path = os.path.join(out_dir, "model_weights.h")
    with open(path, "w") as f:
        f.write("\n".join(lines))
    print(f"  Wrote: {path}  ({len(lines)} lines)")


