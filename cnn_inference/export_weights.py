def write_gelu_consts(model, out_dir):
    """Extract GELU constants (sqrt(2), 1.0, 0.5) from Constant nodes."""
    graph = model.graph

    # Find constant nodes and their outputs
    const_map = {}
    for node in graph.node:
        if node.op_type == "Constant":
            for a in node.attribute:
                if a.name == "value":
                    val = onnx.numpy_helper.to_array(a.t)
                    const_map[node.output[0]] = val

    lines = []
    lines.append("/* Auto-generated GELU constants from ONNX model */")
    lines.append("#ifndef GELU_CONSTS_H")
    lines.append("#define GELU_CONSTS_H")
    lines.append("")

    # Collect unique GELU blocks and their constants
    # Pattern: {divisor, adder, multiplier} from each activation block
    gelu_blocks = {}
    for i, node in enumerate(graph.node):
        if node.op_type == "Div" and "act" in node.input[1] or \
           node.op_type == "Div" and "stem" in node.input[1]:
            div_by = node.input[1]
            if div_by in const_map:
                val = const_map[div_by].item()
                lines.append(f"// GELU sqrt(2) divisor from {div_by}")
                lines.append(f"#define GELU_SQRT2  {val:.8f}f")
                break

    # General case: write all constants for reference
    lines.append("")
    lines.append("/* All GELU parameters (extracted from model, should be identical across blocks) */")

    seen_vals = set()
    for name, val in sorted(const_map.items()):
        if val.size == 1:
            v = val.item()
            c_name = sanitize(name)
            # Only show unique values
            if v not in seen_vals:
                seen_vals.add(v)
                lines.append(f"// {name}")
                lines.append(f"#define {c_name}  {v:.8f}f")
                lines.append("")

    lines.append("#endif /* GELU_CONSTS_H */")

    os.makedirs(out_dir, exist_ok=True)
    path = os.path.join(out_dir, "gelu_consts.h")
    with open(path, "w") as f:
        f.write("\n".join(lines))
    print(f"  Wrote: {path}  ({len(lines)} lines)")


def main():
    print("Loading model...")
    model = onnx.load(MODEL_PATH)
    print(f"  IR v{model.ir_version}, opset {model.opset_import[0].version}")
    print()

    write_weights(model, OUT_DIR)
    write_meta(model, OUT_DIR)
    write_gelu_consts(model, OUT_DIR)

    print()
    print("Done. Ready for C compilation.")

if __name__ == "__main__":
    main()
